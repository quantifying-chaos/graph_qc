def to_table(text):
    li = [[], [], [], []]
    counter = 0
    is_digit = False
    for line in text.splitlines():
        if line == "":
            continue
        if line[0].isdigit():
            li[counter].append(line)
            is_digit = True
        else:
            if is_digit:
                counter += 1
            is_digit = False

    buf = ""
    for i in range(len(li[0])):
        buf += str(i) + " " + "& "
        for j in range(4):
            if len(li[j]) > i:
                buf += str(li[j][i]) + " "
            else:
                buf += " - "
            if j != 3:
                buf += "& "
        buf += r"\\"
        buf += "\n"

    print(buf)


